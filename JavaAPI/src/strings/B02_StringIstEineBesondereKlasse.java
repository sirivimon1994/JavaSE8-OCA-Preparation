package strings;

public class B02_StringIstEineBesondereKlasse {

	public static void main(String[] args) {
		/*
		 * String ist zwar eine Klasse aus der Standardbibliothek, 
		 * aber in Java wurde sogar in der Sprache String-Support
		 * integriert:
		 */
		
		/*
		 * String-Literale
		 */
		
		String s1 = "Mo";

		/*
		 * Konkatenations-Operator
		 */
		
		String s2 = s1 + "ntag";
		
		/*
		 * String-Pool
		 */
	}

}
